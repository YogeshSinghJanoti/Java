public class LogicalOperators {
    public static  void main(String[] args){
        System.out.println(64 > 8 || 12 > 9);// Logical OR OPERATOR
        System.out.println(64 > 8 && 12 > 9);// Logical AND OPERATOR
        System.out.println(70 > 13 && 40 > 50);// Logical AND OPERATOR
        System.out.println(70 > 13 || 40 > 50);// Logical OR OPERATOR
        System.out.println(!(12 > 4));// NOT OPERATOR
    }
}