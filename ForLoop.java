public class ForLoop {
    public static void main(String[] args) {
        // Printing number from 0 to 10 using for loop.
        for (int i = 0;i <= 10;i++){
            System.out.println(i);
        }
        
        // Printing number from 10 to 0 using for loop.
        for (int i = 10;i >= 0;i--){
            System.out.println(i);
        }
    }
}
