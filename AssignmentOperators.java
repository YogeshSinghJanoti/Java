public class AssignmentOperators {
    public static void main(String[] args){
        int a = 10;
        int b = a;
        System.out.println(a);
        System.out.println(b);
        int c = 10;
        c += 12;
        System.out.println(c);
        int d = 10;
        d -= 5;
        System.out.println(d);
        int e = 10;
        e *= 7;
        System.out.println(e);
        int f = 10;
        f /= 2;
        System.out.println(f);
    }
}